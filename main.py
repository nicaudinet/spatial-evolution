def play_lattice(lat, mistake, rounds):
    n = len(lat)
    total_score = []
    for i in range(n):
        total_score.append([])
        for j in range(n):
            total_score[i].append(play_neighbors(lat, i, j, mistake, rounds))
    return total_score




